def clock():
    while 1:
        try:
            clear()
            ctime = datetime.now()
            translate(" : ".join([(str(i) if len(str(i)) == 2 else "0" + str(i)) for i in [ctime.hour, ctime.minute, ctime.second]]))
            sleep(0.5)
        except KeyboardInterrupt:
            clear()
            break

